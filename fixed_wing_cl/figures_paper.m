% %% plots_paper.m
% Paper-ready plotting script with a clean IEEE-style layout.
% Required workspace variables:
%   t_total, logs

close all;

if ~exist('t_total', 'var')
    error('Variable "t_total" was not found in the workspace.');
end
if ~exist('logs', 'var')
    error('Variable "logs" was not found in the workspace.');
end
if ~exist('metrics', 'var')
    error('Variable "metrics" was not found in the workspace.');
end

%% Logged signals
time_vector = t_total;

uav_x_position = logs.x;
uav_y_position = logs.y;
uav_altitude = logs.h;

reference_x_position = logs.ref_cont(:,1);
reference_y_position = logs.ref_cont(:,2);
reference_altitude = logs.ref_cont(:,3);

ground_speed = logs.Vg;
flight_path_angle_deg = rad2deg(logs.gamma);
total_lyapunov_value = logs.Vtot;
euclidean_distance = metrics.ED;

engine_thrust = logs.Th_d_zoh;
g_load = logs.ng_d_zoh;
bank_angle_deg = rad2deg(logs.phib_d_zoh);

%% Constraint values
constraint_values = uav_constraints();

%% Style setup
figure_font_name = 'Times New Roman';
axis_font_size = 10;
legend_font_size = 9;
title_font_size = 11;

main_line_width = 1.8;
reference_line_width = 1.8;
limit_line_width = 1.5;
axes_line_width = 1.1;
marker_size_value = 7;

uav_color = [0.00 0.20 0.90];
reference_color = [0.85 0.45 0.10];
limit_color = [1.00 0.20 0.20];
start_color = [0.00 0.70 0.00];
end_color = [0.90 0.00 0.00];
grid_color = [0.85 0.85 0.85];
hil_line_color = [0.00 0.10 1.00];
