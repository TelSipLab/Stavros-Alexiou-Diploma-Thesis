    'o', 'Color', start_color, 'MarkerFaceColor', start_color, 'MarkerSize', marker_size_value);
final_position_handle = plot3(uav_x_position(end), uav_y_position(end), uav_altitude(end), ...
    'o', 'Color', end_color, 'MarkerFaceColor', end_color, 'MarkerSize', marker_size_value);
grid off;
axis tight;
view(38, 24);
pbaspect([1 1 0.65]);
xlabel('x [m]', 'FontName', figure_font_name, 'FontSize', axis_font_size, 'FontWeight', 'bold');
ylabel('y [m]', 'FontName', figure_font_name, 'FontSize', axis_font_size, 'FontWeight', 'bold');
zlabel('h [m]', 'FontName', figure_font_name, 'FontSize', axis_font_size, 'FontWeight', 'bold');
legend([uav_trajectory_handle, reference_legend_handle, initial_position_handle, final_position_handle], ...
    {'UAV trajectory', 'Reference trajectory', 'Initial UAV position', 'Final UAV position'}, ...
    'Position', [0.62 0.33 0.23 0.14], 'FontName', figure_font_name, ...
    'FontSize', legend_font_size, 'FontWeight', 'bold', 'Box', 'on', 'NumColumns', 1);
set(gca, 'FontName', figure_font_name, ...
    'FontSize', axis_font_size, ...
    'FontWeight', 'bold', ...
    'LineWidth', axes_line_width, ...
    'GridColor', grid_color, ...
    'GridAlpha', 0.9, ...
    'Box', 'on', ...
    'Layer', 'top', ...
    'Position', [0.12 0.16 0.75 0.76]);

print(trajectory_3d_figure, fullfile(output_directory, 'fig1_3d_trajectory.eps'), ...
    '-depsc', '-painters');

