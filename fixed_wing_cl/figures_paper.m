time_plot_end = 220;

transient_altitude_lower = 69.5;
transient_altitude_upper = 80.5;
transient_index_end = find(uav_altitude >= 79.8, 1, 'first');
if isempty(transient_index_end)
    transient_index_end = min(numel(time_vector), max(10, round(0.10 * numel(time_vector))));
end

reference_alignment_tolerance = 0.03;
reference_alignment_extension = find(abs(uav_altitude(transient_index_end:end) - ...
    reference_altitude(transient_index_end:end)) <= reference_alignment_tolerance, 1, 'first');
if ~isempty(reference_alignment_extension)
    transient_index_end = transient_index_end + reference_alignment_extension - 1;
end
transient_altitude_plot_end_time = 12;

transient_x_data = uav_x_position(1:transient_index_end);
transient_y_data = uav_y_position(1:transient_index_end);
transient_reference_x_data = reference_x_position(1:transient_index_end);
transient_reference_y_data = reference_y_position(1:transient_index_end);
transient_time_data = time_vector(1:transient_index_end);
transient_altitude_data = uav_altitude(1:transient_index_end);
transient_reference_altitude_data = reference_altitude(1:transient_index_end);
transient_altitude_plot_index_end = find(time_vector <= transient_altitude_plot_end_time, 1, 'last');
if isempty(transient_altitude_plot_index_end)
    transient_altitude_plot_index_end = numel(time_vector);
end
transient_altitude_time_data = time_vector(1:transient_altitude_plot_index_end);
transient_altitude_plot_data = uav_altitude(1:transient_altitude_plot_index_end);
transient_reference_altitude_plot_data = reference_altitude(1:transient_altitude_plot_index_end);
initial_zoom_time_end = transient_time_data(end);
initial_zoom_vg_lower = min(ground_speed(1:transient_index_end)) - 0.3;
initial_zoom_vg_upper = max(ground_speed(1:transient_index_end)) + 0.3;
initial_zoom_thrust_lower = min(engine_thrust(1:transient_index_end)) - 3.0;
initial_zoom_thrust_upper = max(engine_thrust(1:transient_index_end)) + 3.0;

transient_x_margin = max(5, 0.10 * max(abs(transient_x_data - mean(transient_x_data))));
transient_y_margin = max(5, 0.10 * max(abs(transient_y_data - mean(transient_y_data))));

output_directory = fullfile(fileparts(mfilename('fullpath')), 'figures_paper');
if ~exist(output_directory, 'dir')
    mkdir(output_directory);
end

hil_data_file = fullfile(fileparts(mfilename('fullpath')), 'computational_times.csv');

%% Figure 1: 3D trajectory
trajectory_3d_figure = figure('Color', 'w');
set(trajectory_3d_figure, 'Units', 'centimeters');
trajectory_3d_figure.Position(3:4) = [14 9.5];
set(trajectory_3d_figure, 'PaperPositionMode', 'auto');

uav_trajectory_handle = plot3(uav_x_position, uav_y_position, uav_altitude, ...
    '-', 'Color', uav_color, 'LineWidth', main_line_width);
hold on;
plot3(reference_x_position, reference_y_position, reference_altitude, ...
    '--', 'Color', reference_color, 'LineWidth', main_line_width);
